#include <iostream>

// list instructions for a computer to go to the bathroom
// layout:
// https://i.etsystatic.com/38352284/r/il/192ae3/4313178793/il_794xN.4313178793_hrgp.jpg
int main() {
  std::cout << "walk in through the living room door\n";
  std::cout << "keep walking till you find an opening by your right\n";
  std::cout << "turn into that opening.\n";
  std::cout << "note the doors on your right and left sides. they are not the "
               "doors to go into\n";
  std::cout << "walk past them. continue till you find yourself between "
               "another set of doors and directly in front of a door.\n";
  std::cout
      << "turn to the door on you left. open it. that is the bathroom door\n";
  std::cout << "walk in and do your business.\n";
}
